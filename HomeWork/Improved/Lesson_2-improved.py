import time
import random

# Простая текстовая игра про поиск сокровищ

print("=" * 50)
print("ПРИКЛЮЧЕНИЕ НА КОРАБЛЕ")
print("=" * 50)

# Получаем имя игрока
name = input("Как вас зовут, капитан? ")
print(f"\nОтлично, капитан {name}! Начинаем плавание!")

if name.lower() == "максим":
    print("Опа, Максим мурино")


time.sleep(1)
print("\n" + "-" * 50)
print("ИСТОРИЯ:")
print(f"Вы, капитан {name}, плывете на своем корабле в поисках")
print("легендарного сокровища. Но внезапный Максим сбил вас с курса!")
print("Теперь вы должны решить, куда плыть дальше.")
print("-" * 50)

time.sleep(2)
print("\n ПРАВИЛА ИГРЫ:")
print("• У вас есть 3 попытки")
print("• Выберите одно из направлений:")
print("  север, юг, запад, восток")
print("• Каждое направление ведет к разному результату!")
print()


attempts = 3  # Количество попыток
score = 0     # Счет игрока

while attempts > 0:
    print(f"\n  Осталось попыток: {attempts}")
    print(f" Ваш счет: {score}")
    
    
    while True:
        print("\nКуда плывем, капитан?")
        print("(север/юг/запад/восток)")
        choice = input("> ").lower().strip()
        
        
        if choice in ["север", "юг", "запад", "восток", "с", "ю", "з", "в"]:
            break
        else:
            print("Ты что макан, я не шарю что ты написал там, попробуй ещё раз.")
    
    
    if choice == "с": choice = "север"
    elif choice == "ю": choice = "юг"
    elif choice == "з": choice = "запад"
    elif choice == "в": choice = "восток"
    
    # Добавляем случайное событие (чтобы было интереснее)
    events = [
        "встретили стаю дельфинов",
        "попали в легкий шторм",
        "увидели летающих рыб",
        "нашли старую карту в бутылке",
        "встретили торговое судно"
        "встретили Максима который не сделал ДЗ"
    ]
    random_event = random.choice(events)
    
    print(f"\n  Вы плывете на {choice}...")
    time.sleep(1)
    print(f" По пути вы {random_event}!")
    time.sleep(1)
    
    # Проверяем результат
    print("\n" + "~" * 50)
    
    if choice == "север":
        print("  Вы доплыли до северных фогов!")
        print(" Нашли сундук с золотом викингов!")
        print(" +100 очков!")
        score += 100
        attempts -= 1
        
    elif choice == "юг":
        print("  Вы достигли тропических островов!")
        print(" Но там были пираты... Они забрали все!")
        print(" Потеряна 1 попытка")
        attempts -= 1
        
    elif choice == "запад":
        print(" Вы плыли на закат")
        print(" Ничего не нашли, возвращаемся назад")
        print(" Попытка не потрачена!")
        
        
    elif choice == "восток":
        print("  Вы достигли острова сокровищ!")
        print(" НАШЛИ ГЛАВНОЕ СОКРОВИЩЕ!")
        print(" +200 очков!")
        score += 200
        attempts -= 1
    
    print("~" * 50)
    time.sleep(2)
    
    
    if attempts > 0:
        print("\nПродолжаем поиски?")
        print("(да/нет)")
        continue_choice = input("> ").lower().strip()
        
        if continue_choice in ["нет", "н", "стоп", "хватит"]:
            print("\n Лан")
            break


print("\n" + "=" * 50)
print(" ИГРА ЗАКОНЧЕНА!")
print("=" * 50)
print(f" Игрок: {name}")
print(f" Финальный счет: {score}")
print(f" Попыток использовано: {3 - attempts}")

# 6. Определяем уровень успеха
if score >= 200:
    print(" Звание: ЛЕГЕНДАРНЫЙ КАПИТАН")
elif score >= 100:
    print(" Звание: ОПЫТНЫЙ МОРЕПЛАВАТЕЛЬ")
else:
    print(" Звание: НАЧИНАЮЩИЙ МОРЯК")

print("\nСпасибо за игру! ")
print("=" * 50)